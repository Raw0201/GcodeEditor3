def gen_mazak(data: list) -> list:
    """Genera los códigos para fresadora Mazak

    Args:
        data (list): Lista de datos a procesar

    Returns:
        list: Lista de líneas de tape generadas
    """
    sub, dpt, cut, dia, cqt, rqt, csp, rsp, xsp, ysp, xdm, ydm, xcm, ycm, blk = data.values()
    blk = "/" if blk else ""
    iu_space = fspace_ui()

    rep = math.ceil(dpt / cut)
    zin = (rep * cut) + 0.005
    zsc = fnum3(zin + 0.05)
    xin = fnum4((dia / 2) + 0.05)
    yin = fnum4(ydm - (dia / 2) + 0.05)
    zin = fnum3(zin)
    rep = f"L{rep}" if rep > 1 else ""

    lines1 = [
            f"{blk}G90G00X{xin}Y-{yin}Z{zsc}",
            f"{blk}G01Z{zin}F.02",
            f"{blk}M98P{sub}{rep}",
            f"{blk}G90G00Z{zsc}",
        ]
    lines2 = [iu_space for _ in lines1]

    return [lines1, lines2]
